package model;

public class ModelObj<T> {
	
	private T eletento;
	private int id;
	
	
	public T getEletento() {
		return eletento;
	}
	public void setEletento(T eletento) {
		this.eletento = eletento;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	

}
